# @lc code=start
class Solution:
    def corpFlightBookings(self, bookings: List[List[int]], n: int) -> List[int]:
        diff = [0] * (n + 1)
        for first, last, seats in bookings:
            diff[first - 1] += seats
            diff[last] -= seats
        ans = [0] * n
        curr = 0
        for i in range(n):
            curr += diff[i]
            ans[i] = curr
        return ans
# @lc code=end

